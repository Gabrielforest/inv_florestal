# Interpretação Intervalo de Confiança ------------------------------------

Considerando um nível de significância de 5%  
existe 95% de confiança de que a média populacional
se encontra no intervalo de confiança entre o
limite inferior e superior, com 5% de chance
de eu ter sido iludido pela minha amostra.

Logo, temos 100% de confiança que a média amostral
está dentro do intervalo de confiança.


# Interpretação Amostragem Casual Simples ---------------------------------

Na amostragem casual simples não há restricao a casualizaçao
A ACS é usada em áreas homogêneas de fácil acesso, 
áreas heterogêneas são ruins para ACS pois podem ser sorteadas parcelas 
que não representam o todo, e áreas de difícil acesso encarecem o processo
de inventário logisticamente quando usamos ACS.


